SELECT
EQUIPMENT_MAKE,
EQUIPMENT_MODEL,
EQUIPMENT_MAINTENANCE_INTERVAL_MONTHS,
EQUIPMENT_MAINTENANCE_EVENT
FROM
EQUIPMENT_MAINTENANCE_INTERVALS
INNER JOIN EQUIPMENT_MAINTENANCE_EVENTS
ON EQUIPMENT_MAINTENANCE_EVENTS.EQUIPMENT_MAINTENANCE_EVENTS_PK = EQUIPMENT_MAINTENANCE_INTERVALS.EQUIPMENT_MAINTENANCE_EVENT_PK